    public String decodeBase64(String encodedPass) {
        byte[] decodedBytes = Base64.getDecoder().decode(encodedPass);
        return new String(decodedBytes);
    }
    
    public String encodeBase64(String password) {
        return Base64.getEncoder().encodeToString(password.getBytes());
    }
    
    public void View() {
        try {
            Scanner sc = new Scanner(new File("./Data/TaiKhoan.txt"));
            int n = 0;
            boolean accountFound = false;
            while (sc.hasNextLine()) {
                String username = sc.nextLine(); // Đọc username
                String password = sc.nextLine(); // Đọc password
                String name = sc.nextLine();     // Đọc họ tên
                if (n == pos) {
                    this.txtUser.setText(username);
                    this.txtPass.setText(decodeBase64(password));
                    this.txtName.setText(name);
                    accountFound = true;
                    break;
                }
                n++;
            }
            sc.close();
            if (!accountFound) {
                this.txtUser.setText("");
                this.txtPass.setText("");
                this.txtName.setText("");
            }
        } catch (Exception e) {}
        OnOff(true, false);
    }
    
    public void ViewTable() {
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"STT", "Họ và Tên" , "Username", "Password"});

        model.setNumRows(0); // Xóa dữ liệu cũ trong bảng
        int n = 1;

        try {
            File file = new File("./Data/TaiKhoan.txt");
            if (!file.exists())
                return;

            Scanner sc = new Scanner(file);
            while (sc.hasNextLine()) {
                String user = sc.nextLine();  // Đọc username
                String pass = sc.nextLine();  // Đọc password
                String name = sc.nextLine();  // Đọc họ tên
                model.addRow(new Object[]{n++, name, user, decodeBase64(pass)});
            }
            sc.close();
        } catch (Exception e) {}
        this.tblAccount.setModel(model);
        this.tblAccount.setRowHeight(22);
        this.tblAccount.getColumnModel().getColumn(0).setPreferredWidth(100); // STT
        this.tblAccount.getColumnModel().getColumn(1).setPreferredWidth(300); // Họ và Tên
        this.tblAccount.getColumnModel().getColumn(2).setPreferredWidth(200); // Username
        this.tblAccount.getColumnModel().getColumn(3).setPreferredWidth(200); // Password
        DefaultTableCellRenderer cellRenderer = new DefaultTableCellRenderer();
        cellRenderer.setHorizontalAlignment(JLabel.CENTER);
        tblAccount.getColumnModel().getColumn(0).setCellRenderer(cellRenderer);
        tblAccount.getColumnModel().getColumn(2).setCellRenderer(cellRenderer);
        tblAccount.getColumnModel().getColumn(3).setCellRenderer(cellRenderer);
        tblAccount.setDefaultEditor(Object.class, null);
        tblAccount.setRowSelectionInterval(pos, pos);
    }
    
