    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        String username = this.txtUser.getText();
        List<String> lines = new ArrayList<>();
        try (BufferedReader br = new BufferedReader(new FileReader("./Data/TaiKhoan.txt"))) {
            String line;
            while ((line = br.readLine()) != null) {
                String existingUsername = line;
                String existingPassword = br.readLine();
                String existingName = br.readLine();
                if (!existingUsername.equals(username)) {
                    lines.add(existingUsername);
                    lines.add(existingPassword);
                    lines.add(existingName);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi khi đọc dữ liệu!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try (FileWriter writer = new FileWriter("./Data/TaiKhoan.txt")) {
            for (String line : lines) {
                writer.write(line + "\n");
            }
            JOptionPane.showMessageDialog(this, "Xóa tài khoản thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi khi lưu thông tin!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        View();
        ViewTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

