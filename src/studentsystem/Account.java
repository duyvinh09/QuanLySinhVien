    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String username = this.txtUser.getText();
        String password = this.txtPass.getText();
        String name = this.txtName.getText();
        
        if (username.isEmpty() || password.isEmpty() || name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try (BufferedReader br = new BufferedReader(new FileReader("./Data/TaiKhoan.txt"))) {
            String line;
            boolean accountExists = false;
            while ((line = br.readLine()) != null) {
                String existingUsername = line;
                String existingPassword = br.readLine();
                String existingName = br.readLine();
                if (existingUsername.equals(username)) {
                    accountExists = true;
                    break;
                }
            }
            if (accountExists) {
                JOptionPane.showMessageDialog(this, "Tài khoản đã tồn tại!", "Thông báo", JOptionPane.WARNING_MESSAGE);
                return;
            }
        } catch (Exception e) {return;}

        try (FileWriter writer = new FileWriter("./Data/TaiKhoan.txt", true)) {
            writer.write(username + "\n");
            writer.write(encodeBase64(password) + "\n");
            writer.write(name + "\n");
            JOptionPane.showMessageDialog(this, "Lưu thông tin thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi khi lưu thông tin!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        View();
        ViewTable();
    }//GEN-LAST:event_btnSaveActionPerformed

